            <div align="center"> 
                <b>VISION </b> 
                <p>"Construyendo una comuna saludable"</p>
            </div>
        </th>    
        </thead>
        <tbody>
            <tr>
                <td>
                    <p>Nuestra misión es desarrollar, gestionar 
                        y coordinar los recursos para satisfacer las necesidades 
                        y otorgar prestaciones de salud que competen al nivel primario 
                        de atención de la comuna de Linares, motivando la 
                        partición activa de las organizaciones sociales basadas 
                        en relación de confianza, complementaridad y equidad, 
                        promoviendo estilos de vida saludables, otorgando prestaciones 
                        de salud con oportunidad, integrales con respeto y trato 
                        digno.<br>
                        Nuestro desafío es constituir una micro red integrada 
                        y resolutiva que permita mejorar la oportunidad de atención 
                        con, pertinencia y eficacia, en pro de una permanente construcción 
                        de una comuna saludable. </p>
                    <p>La Reforma de salud en curso en el país, 
                        define el Modelo Integral de Salud como el conjunto de acciones 
                        que promueven y facilitan la atención eficiente, 
                        eficaz y oportuna que se dirige, más que al paciente 
                        o a la enfermedad como hechos aislados, a las personas, 
                        consideradas en su integridad física y mental, insertos 
                        en una sociedad.</p>
                    <p>El quehacer sanitario se basa en un equipo 
                        de salud multidisciplinario y de liderazgo horizontal, que 
                        se hace cargo de la población de un territorio acotado, 
                        aplicando el modelo de salud familiar, es decir, acompaña 
                        a los integrantes de la familia durante todo el ciclo vital, 
                        apoyando los momentos críticos con consejería, 
                        educación y estrategias de promoción de salud, 
                        detectando oportunamente el daño y proporcionando 
                        asistencia clínica y rehabilitación oportuna 
                        en caso de patología aguda o crónica. </p>
                    <p>Al Departamento de Salud Comunal le corresponden 
                        las acciones de atención primaria, esto es la atención 
                        ambulatoria de la población asignada al consultorio 
                        o posta de salud rural de acuerdo a los programas de Ministerio 
                        de Salud<br>
                        En cada establecimiento debe funcionar un Comité 
                        Técnico que asesora la gestión de los Centros 
                        de Salud, así como también la existencia de 
                        un comité de de gestión para listas de espera 
                        de interconsultas. Igual función cumplen los Directores 
                        de establecimientos, con respecto a la administración 
                        y gestión del Departamento de Salud.</p>
                    <p> <br>
                        Existe una Unidad Rural con diez postas que están 
                        a cargo de un Técnico Paramédico de residencia 
                        en el sector. El equipo de Salud Rural, conformado por Médico, 
                        Enfermera, Nutricionista, Asistente Social y Auxiliar Paramédico, 
                        dependientes administrativamente del Departamento Comunal 
                        de Salud que realizan rondas periódicamente programadas 
                        para prestar atención de salud a la población 
                        rural.
                    </p>    
                </td>
