                    <p><img src="<?php echo base_url(); ?>../img/quienes_som.jpg" align="left" style=" width: 60%; height: 60%; border: 5px transparent solid;"/>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;LA MUNICIPALIDAD DE LINARES, FORMA PARTE DE LAS 345 CORPORACIONES EDILICIAS 
                        EXISTENTES A LO LARGO Y ANCHO DEL PAIS, ES UNA CORPORACION AUTONOMA 
                        DE DERECHO PUBLICO CON PERSONALIDAD JURIDICA Y PATRIMONIO PROPIO. 
                        SU FINALIDAD ES SATISFACER LAS NECESIDADES DE LA COMUNIDAD Y ASEGURAR 
                        SU PARTICIPACION EN EL PROGRESO ECONOMICO, SOCIAL Y CULTURAL DE 
                        LA COMUNA.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EL ORIGEN COMO CABILDO, 
                        SE REMONTA AL AÑO 1.265 CUANDO EL REY ALFONSO X EL SABIO, 
                        RECONOCE A SUS HABITANTES CIERTOS FUEROS PARA ORGANIZARSE Y CONFORMAR 
                        AYUNTAMIENTOS, DESTINADOS AL GOBIERNO POLITICO Y JUDICIAL DE LOS 
                        PUEBLOS.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;LA AUTORIDAD MAYOR 
                        ERA EL ALCALDE, PALABRA DERIVADA DEL VOCABLO ARABE "CADÍ", 
                        QUE EN CASTELLANO EQUIVALE A JUEZ, EL CUAL, ERA RESPALDADO POR UN 
                        CUERPO DE CONCEJALES.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EL PRIMER CABILDO SE 
                        CONSTITUYO EN SANTIAGO, EL 11 DE MARZO DE 1541, POR ORDEN DE DON 
                        PEDRO DE VALDIVIA, RAPIDAMENTE ESTA INSTITUCION SE PROPAGO A LO 
                        LARGO DE TODO EL TERRITORIO NACIONAL.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EL CABILDO COLONIAL 
                        VELABA POR LOS SERVICIOS DE LA COMUNIDAD, ADMINISTRABA JUSTICIA, 
                        SE PREOCUPABA POR LA EXISTENCIA DE LAS ESCUELAS, LA PROVICION DE 
                        ALIMENTOS, AGUA Y LA DEFENSA DE LA COMUNIDAD.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TRAS DIVERSOS CAMBIOS 
                        Y TRANSFORMACIONES, EN EL AÑO 1891 EL PRESIDENTE DON MANUEL 
                        JOSE IRARRAZABAL DICTA LA LEY QUE DA AUTONOMIA A LAS COMUNAS, ESTABLECIENDO, 
                        ASI UNA MUNICIPALIDAD MODERNA, DESLIGADA DEL PODER CENTRAL, DEMOCRATICA 
                        Y PARTICIPATIVA, TAL COMO SE LE CONOCE HOY EN DIA.</p>
                    <p></p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;LA MUNICIPALIDAD O 
                        CABILDO DE LINARES, NO HA ESTADO AJENO A TODO ESTE PROCESO Y SU 
                        FUNCIONAMIENTO DATA DESDE LA CREACION DE LA VILLA, EL 23 DE MAYO 
                        DE 1794.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EL PRIMER ALCALDE LINARENSE 
                        ELEGIDO Y QUE REGISTRA LA HISTORIA, FUE DON JOSE DIONISIO VELA, 
                        HECHO ACAECIDO EL 10 DE MAYO DE 1873, DESDE ESA FECHA MUCHOS HAN 
                        SIDO LOS ALCALDES, REGIDORES O CONCEJALES, QUE HASTA HOY, HAN DIRIGIDO 
                        LOS DESTINOS DE ESTA COMUNA.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EL MUNICIPIO DE LINARES, 
                        COMO TAL, ES LA ENTIDAD PUBLICA MAS IMPORTANTE DE LA COMUNA, NO 
                        SOLO POR LA CANTIDAD DE FUNCIONARIOS, CONSIDERANDO LOS TRES ESTAMENTOS: 
                        MUNICIPALIDAD Y LOS SERVICIOS INCORPORADOS A LA GESTION DE SALUD 
                        Y EDUCACION; SINO QUE ADEMAS, POR EL PRESUPUESTO ANUAL QUE ADMINISTRA.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;LA MUNICIPALIDAD ESTA 
                        CONSTITUIDA POR EL ALCALDE MAXIMA AUTORIDAD, SU OBJETIVO ES DIRIGIR, 
                        ADMINISTRAR Y SUPERVIGILAR EL FUNCIONAMIENTO DE LA ORGANIZACIÓN, 
                        EL ALCALDE ES ELEGIDO POR SUFRAGIO UNIVERSAL Y DURA CUATRO AÑOS 
                        EN SU CARGO.</p>
                    <p></p>
                    <p></p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;OTRO ORGANO DE IMPORTANCIA 
                        DE CARÁCTER NORMATIVO, RESOLUTIVO Y FISCALIZADOR, ES EL CONCEJO 
                        MUNICIPAL, ENCARGADO DE HACER EFECTIVA LA PARTICIPACION DE LA COMUNIDAD 
                        LOCAL, ESTE ES PRESIDIDO POR EL ALCALDE Y ESTA CONFORMADO POR 6 
                        CONCEJALES, ELEGIDOS POR SUFRAGIO UNIVERSAL Y DURAN 4 AÑOS 
                        EN SU CARGO.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;POR SU PARTE, EL CONSEJO 
                        ECONOMICO Y SOCIAL COMUNAL (CESCO) DE CARÁCTER CONSULTIVO, 
                        TIENE POR OBJETO, ASEGURAR LA PARTICIPACION DE LAS ORGANIZACIONES 
                        COMUNITARIAS DE CARÁCTER TERRITORIAL Y FUNCIONAL Y DE ACTIVIDADES 
                        RELEVANTES EN EL PROGRESO ECONOMICO, SOCIAL Y CULTURAL DE LA COMUNA</p>
                    <p></p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;LA MUNICIPALIDAD PARA 
                        DAR CONFORMIDAD A TODA SU OPERATORIA ADMINISTRATIVA DE COORDINACION 
                        Y APOYO FUNCIONA A TRAVES DE TODA UNA ESTRUCTURA ORGANIZADA:</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ADMINISTRACION MUNICIPAL, 
                        CARGO DIRECTIVO DE DEPENDENCIA DIRECTA DEL ALCALDE, CUYA LABOR ES 
                        EJERCER LA GESTIÓN EJECUTIVA Y COORDINACION AL INTERIOR DEL 
                        MUNICIPIO.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;JUZGADO DE POLICIA 
                        LOCAL, UNIDAD QUE DEPENDE ADMINISTRATIVAMENTE DE LA MUNICIPALIDAD 
                        Y TECNICAMENTE DEL PODER JUDICIAL A TRAVES DE LA CORTE DE APELACIONES, 
                        SU MISION, ES ADMINISTRAR JUSTICIA EN LA COMUNA DE ACUERDO A LA 
                        COMPETENCIA QUE LE FIJA LA LEY.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;SECRETARIA MUNICIPAL, 
                        DEPENDE DIRECTAMENTE DEL ALCALDE, A TRAVES DE SUS SECCIONES DE GABINETE 
                        SECRETARIA Y PARTES ARCHIVOS Y RECLAMOS, TIENE POR OBJETO ADMINISTRAR 
                        LAS ACTIVIDADES DE SECRETARIA ADMINISTRATIVA DEL ALCALDE, CONCEJO 
                        Y EL CESCO ADEMAS DE DESEMPEÑARSE COMO MINISTRO DE FE EN 
                        TODAS LAS ACTUACIONES MUNICIPALES.</p>
                    <p><br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE CONTROL, DEPENDE DIRECTAMENTE 
                        DEL ALCALDE Y ADEMAS ESTA SUJETA A LA DEPENDENCIA DE LA CONTRALORIA 
                        GENERAL DE LA REPUBLICA, SU TRABAJO ES MEJORAR Y CONTROLAR LA ACCION 
                        MUNICIPAL A TRAVES DEL EXAMEN CRITICO METODICO Y SISTEMATICO DE 
                        LA ORGANIZACIÓN, VERIFICANDO EL CUMPLIMIENTO DE SUS OBJETIVOS, 
                        ACCION QUE CUMPLE A TRAVES DE SUS SECCIONES OPERATIVA ADMINISTRATIVA 
                        Y AUDITORIA Y CONTROL FINANCIERO</p>
                    <p><br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ASESORIA JURIDICA, UNIDAD ENCARGADA 
                        DE APOYAR EN MATERIAS LEGALES AL ALCALDE, CONCEJO Y A LAS UNIDADES 
                        MUNICIPALES, DE TAL MANERA QUE EL ACCIONAR DEL MUNICIPIO SE AJUSTE 
                        AL MARCO LEGAL CORRESPONDIENTE, ADEMAS EN SUS DEPENDENCIAS FUNCIONA 
                        LA OFICINA DEL SERVICIO NACIONAL DEL CONSUMIDOR (SERNAC) QUE ATIENDE 
                        LAS DEMANDAS Y PRESTA ASESORIA LEGAL A LOS CONSUMIDORES.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;SECRETARIA COMUNAL 
                        DE PLANIFICACION (SECPLAN), ESTA DIRECCION TIENE A SU CARGO LOS 
                        DEPARTAMENTOS DE PLANIFICACION Y PROYECTOS Y SECTORIALISTAS Y ESTADISTICAS, 
                        A TRAVES DE LOS CUALES ASESORA AL ALCALDE Y AL CONCEJO, EN ESTUDIOS 
                        Y EVALUACION DE PROYECTOS, Y EN MATERIAS PROPIAS DE LA COMPETENCIA 
                        DE AMBOS ORGANOS MUNICIPALES.</p>
                    <p></p>
                    <p><br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE RELACIONES PUBLICAS, 
                        ESTA UNIDAD TIENE A SU CARGO LAS SECCIONES CEREMONIAL Y PROTOCOLO, 
                        COMUNICACIÓN SOCIAL Y AUDIOVISUAL, ESTAMENTOS A TRAVES DE 
                        LOS CUALES DEBE LOGRAR UN DESARROLLO EFICIENTE DE LAS COMUNICACIONES 
                        ENTRE EL MEDIO, LA COMUNIDAD Y LA MUNICIPALIDAD, ADEMAS DE UNA FLUIDA 
                        INTERACCION CON LA COMUNIDAD</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE ADMINISTRACION 
                        Y FINANZAS, SUS 3 DEPARTAMENTOS, 10 SECCIONES Y 8 OFICINAS, TIENEN 
                        POR MISION OPTIMIZAR EL USO DE RECURSOS FINANCIEROS, MEDIANTE LA 
                        ADMINISTRACION EFICIENTE DE LA ACTIVIDAD FINANCIERA, CONTABLE Y 
                        PRESUPUESTARIA, ASIMISMO, DE PROVEER Y ADMINISTRAR LOS RECURSOS 
                        MATERIALES NECESARIOS PARA UNA ADECUADA GESTION MUNICIPAL.</p>
                    <p><br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE TRANSITO Y TRANSPORTE 
                        PUBLICO, ESTA DIRECCION TIENE A SU CARGO EL OTORGAMIENTO DE LICENCIAS 
                        DE CONDUCIR, PERMISOS DE CIRCULACION E INGENIERIA Y OPERACIONES, 
                        SIENDO SU OBJETIVO LA GESTION DEL TRANSITO, PRESTANDO UN SERVICIO 
                        COMPLETO Y EFICIENTE A LA COMUNIDAD, CON LA MANTENCION ADECUADA 
                        DE LAS SEÑALIZACIONES DEL TRANSITO EN LA COMUNA.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE OBRAS 
                        MUNICIPALES, ESTE ESTAMENTO TIENE COMO LABOR VELAR POR EL CUMPLIMIENTO 
                        DE LAS DISPOCIONES DE LA LEY GENERAL DE URBANISMO Y CONSTRUCCION, 
                        DEL PLAN REGULADOR COMUNAL Y DEMAS ORDENANZAS VIGENTES, PROPOSITOS 
                        A LOS CUALES DA CUMPLIMIENTO, MEDIANTE, LOS DEPARTAMENTOS, ADMINISTRATIVO 
                        E INSPECCION TECNICA DE OBRAS Y SUS SECCIONES DE CATASTROS Y ARCHIVOS 
                        Y PERMISOS Y RECEPCIONES.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE DESARROLLO 
                        COMUNITARIO (DIDECO), ESTA UNIDAD ESTA ORGANIZADA EN 4 DEPARTAMENTOS 
                        Y 5 SECCIONES, SIENDO SU TRABAJO ASESORAR AL ALCALDE Y AL CONCEJO 
                        EN TODO LO RELATIVO AL DESARROLLO DE LA COMUNIDAD DE LINARES, EN 
                        EL AMBITO SOCIAL, CULTURAL Y ECONOMICO, DANDO ESPECIAL ENFASIS EN 
                        EL MEJORAMIENTO DE LAS CONDICIONES DE VIDA Y EN LA INTEGRACION Y 
                        PARTICIPACION DE LA COMUNIDAD.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE DESARROLLO 
                        ECONOMICO LOCAL (OMDEL), DE RECIENTE CREACION, TIENE A SU CARGO 
                        LAS OFICINAS DE CAPACITACION Y EMPLEO, DESARROLLO PRODUCTIVO Y TURISMO 
                        LOCAL, SU FUNCION ES PLANIFICAR, COORDINAR, SUPERVISAR Y EVALUAR 
                        LAS ACCIONES DEL MUNICIPIO QUE TIENDAN AL DESARROLLO, TANTO ECONOMICO, 
                        COMO LABORAL DE LA COMUNA, GENERANDO CANALES DE COMUNICACIÓN 
                        ENTRE EL MUNICIPIO Y EL SECTOR PRODUCTIVO, Y OTRAS ORGANIZACIONES 
                        RELACIONADAS, SEAN ESTAS PUBLICAS O PRIVADAS.</p>
                    <p><br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE SERVICIOS GENERALES, 
                        TIENE LA MISION DE APOYAR LA ENTREGA DE SERVICIOS AL RESTO DE LAS 
                        UNIDADES MUNICIPALES Y A LA COMUNIDAD EN RELACION A LA MOVILIZACION, 
                        EVENTOS ESPECIALES, MANTENCION Y SERVICIOS MENORES. ASIMISMO, ESTA 
                        ENCARGADA DE VELAR POR EL ASEO Y ORNATO, Y LA MANTENCION DE LOS 
                        BIENES NACIONALES DE USO PUBLICO, EXISTENTES EN LA COMUNA.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DIRECCION DE SERVICIOS 
                        INCORPORADOS A LA GESTION, SON DE SU CARGO LOS DEPARTAMENTOS MUNICIPALES 
                        DE SALUD Y EDUCACION, TENIENDO POR OBJETO ASESORAR AL ALCALDE Y 
                        AL CONCEJO EN LA FORMULACIOMN DE POLITICAS RELATIVAS A ESTAS AREAS.</p>
                    <p></p>
                    <p><br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EN ESTE SENTIDO EL DEPARTAMENTO DE 
                        SALUD, A TRAVES DE SUS SECCIONES TECNICA Y ESTADISTICAS, FARMACIA, 
                        LABORATORIO Y CONTROL GESTION Y ESTADISTICA, DEBE VELAR PORQUE SE 
                        BRINDE UNA ATENCION DE CALIDAD A LOS USUARIOS POTENCIANDO Y FORTALECIENDO 
                        LA ARTICULACION DE REDES DE APOYO INTERSECTORIALES.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;POR SU PARTE, EL DEPARTAMENTO 
                        MUNICIPAL DE EDUCACION (DAEM), ESTA ENCARGADO DE ADMINISTRAR, PLANIFICAR, 
                        PROGRAMAR, EJECUTAR, CONTROLAR Y FINANCIAR LA FORMULACION DE PROYECTOS 
                        Y EJECUCION DE POLITICAS RELATIVAS A LA EDUCACION MUNICIPALIZADA.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EN LOS EFECTOS DE COORDINAR 
                        EL FUNCIONAMIENTO INTERNO DEL MUNICIPIO Y ANALIZAR EL DESARROLLO 
                        DE LOS DIVERSOS PLANES Y PROYECTOS COMUNALES, EL ALCALDE SE REUNE 
                        PERIODICAMENTE CON EL COMITÉ TECNICO OPERATIVO, CONFORMADO 
                        POR LOS DIRECTORES Y JEFATURAS DE LOS DIFERENTES SERVICIOS MUNICIPALES.</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ESTA ES LA ESTRUCTURA, 
                        EL TRABAJO, Y EL COMPROMISO, QUE A DIARIO DESARROLLAN LOS HOMBRES 
                        Y MUJERES DE LA CORPORACION EDILICIA, CUYA MISION SE ABOCA A PROMOVER, 
                        IMPULSAR, DISEÑAR Y GESTIONAR EL DESARROLLO INTEGRAL Y SUSTENTABLE 
                        DE LA COMUNA DE LINARES, A TRAVES, DE UNA ADMINISTRACION MODERNA, 
                        EFICIENTE Y TRANSPARENTE CON LA ACTIVA PARTICIPACION DE TODOS SUS 
                        HABITANTES.</p>
